public class SRPViolation {

    public int addCalculation(int num1, int num2){

        return num1 + num2;
    }

    public int subCalculation(int num1, int num2){

        return num1 - num2;
    }

    public int multiplyCalculation(int num1, int num2){

        return num1 * num2;

    }

    public int divideCalculation(int num1, int num2){

        return num1 / num2;
    }
}
